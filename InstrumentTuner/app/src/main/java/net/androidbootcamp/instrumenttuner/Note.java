package net.androidbootcamp.instrumenttuner;

public class Note
{
    String name;
    double frequency;
    double minRange;
    double maxRange;

    public Note(String name, double frequency, double minRange, double maxRange)
    {
        this.name = name;
        this.frequency = frequency;
        this.minRange = minRange;
        this.maxRange = maxRange;
    }


}